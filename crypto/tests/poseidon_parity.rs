/// Test Merkle root computation
#[test]
fn test_merkle_root_computation() {
    println!("=== Merkle Root Test ===\n");
    
    // Create 4 leaves
    let leaves: Vec<[u8; 32]> = (0..4u8)
        .map(|i| {
            let mut leaf = [0u8; 32];
            leaf[0] = i;
            leaf
        })
        .collect();
    
    // Compute root using native Merkle
    let tree = MerkleTree::new(&leaves);
    let native_root = tree.root();
    
    println!("Native Merkle root (4 leaves): 0x{}", hex::encode(&native_root));
    println!("Tree depth: {}", tree.len());
    
    // Test that we can verify a proof
    let proof = tree.proof(0);
    assert!(MerkleTree::verify_proof(&native_root, &leaves[0], &proof));
    println!("✓ Merkle proof verified");
}

/// Test determinism
#[test]
fn test_hash_determinism() {
    let a = [1u8; 32];
    let b = [2u8; 32];
    
    let h1 = PoseidonHasher::hash_two(&a, &b);
    let h2 = PoseidonHasher::hash_two(&a, &b);
    
    assert_eq!(h1, h2, "Hash must be deterministic");
    println!("✓ Hash is deterministic");
}

/// Test collision resistance (different inputs produce different outputs)
#[test]
fn test_collision_resistance() {
    let pairs = [
        ([0u8; 32], [0u8; 32]),
        ([0u8; 32], [1u8; 32]),
        ([1u8; 32], [0u8; 32]),
        ([1u8; 32], [1u8; 32]),
    ];
    
    let mut hashes = std::collections::HashSet::new();
    
    for (a, b) in &pairs {
        let hash = PoseidonHasher::hash_two(a, b);
        let inserted = hashes.insert(hash);
        assert!(inserted, "Hash collision detected!");
    }
    
    println!("✓ No collisions in {} test pairs", pairs.len());
}

/// Benchmark hashing performance
#[test]
fn bench_hash_performance() {
    use std::time::Instant;
    
    let a = [1u8; 32];
    let b = [2u8; 32];
    
    let iterations = 10_000;
    let start = Instant::now();
    
    for _ in 0..iterations {
        let _ = PoseidonHasher::hash_two(&a, &b);
    }
    
    let elapsed = start.elapsed();
    let per_hash = elapsed.as_nanos() / iterations;
    
    println!("Hash performance:");
    println!("  {} hashes in {:?}", iterations, elapsed);
    println!("  {} ns per hash", per_hash);
    println!("  {:.2} hashes/sec", iterations as f64 / elapsed.as_secs_f64());
}

#[test]
fn test_merkle_tree_sizes() {
    println!("=== Merkle Tree Sizes ===\n");
    
    for num_leaves in [1, 2, 4, 8, 16, 32, 64, 128] {
        let leaves: Vec<[u8; 32]> = (0..num_leaves)
            .map(|i| {
                let mut leaf = [0u8; 32];
                leaf[0] = (i % 256) as u8;
                leaf
            })
            .collect();
        
        let tree = MerkleTree::new(&leaves);
        println!("  {} leaves -> depth {}, root: 0x{}...", 
                 num_leaves,
                 (num_leaves as f64).log2().ceil() as usize,
                 hex::encode(&tree.root()[..8]));
    }
}