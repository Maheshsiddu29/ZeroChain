//! Poseidon Hash Parity Test
//!
//! CRITICAL: This test ensures that the native Poseidon implementation
//! in crypto/ matches the in-circuit implementation in circuits/transfer/.
//!
//! If this test fails, ALL proofs will fail verification on chain.
//!
//! Run: cargo test -p zc-crypto --test poseidon_parity -- --nocapture

use zc_crypto::poseidon::PoseidonHasher;
use zc_crypto::merkle::MerkleTree;
use serde::Deserialize;
use std::fs;
use std::path::PathBuf;

/// Structure matching circuit's Poseidon test vector output
#[derive(Debug, Deserialize)]
struct PoseidonVectors {
    description: String,
    field: String,
    vectors: Vec<PoseidonVector>,
}

#[derive(Debug, Deserialize)]
struct PoseidonVector {
    inputs: Vec<String>,
    output: String,
}

/// Load Poseidon test vectors from circuit
fn load_vectors() -> Result<PoseidonVectors, String> {
    // Look for vectors generated by the circuit
    let paths = vec![
        PathBuf::from(env!("CARGO_MANIFEST_DIR"))
            .join("..")
            .join("circuits")
            .join("transfer")
            .join("tests")
            .join("fixtures")
            .join("poseidon_vectors.json"),
        PathBuf::from("circuits/transfer/tests/fixtures/poseidon_vectors.json"),
        PathBuf::from("tests/fixtures/poseidon_vectors.json"),
    ];
    
    for path in &paths {
        if path.exists() {
            let content = fs::read_to_string(path)
                .map_err(|e| format!("Failed to read file: {}", e))?;
            let vectors: PoseidonVectors = serde_json::from_str(&content)
                .map_err(|e| format!("Failed to parse JSON: {}", e))?;
            return Ok(vectors);
        }
    }
    
    Err("Poseidon test vectors not found".to_string())
}

/// Parse hex string to bytes
fn hex_to_bytes(hex: &str) -> Vec<u8> {
    let hex = hex.strip_prefix("0x").unwrap_or(hex);
    hex::decode(hex).expect("Invalid hex string")
}

/// Parse hex to 32-byte array
fn hex_to_hash256(hex: &str) -> [u8; 32] {
    let bytes = hex_to_bytes(hex);
    let mut hash = [0u8; 32];
    let len = bytes.len().min(32);
    hash[..len].copy_from_slice(&bytes[..len]);
    hash
}

#[test]
#[ignore] // Requires test vectors to be generated first
fn test_poseidon_parity_with_circuit() {
    println!("=== Poseidon Hash Parity Test ===\n");
    
    let vectors = match load_vectors() {
        Ok(v) => v,
        Err(e) => {
            println!("⚠ Test vectors not found: {}", e);
            println!("Generate them with: cargo test -p transfer-circuit --test generate_vectors");
            return;
        }
    };
    
    println!("Field: {}", vectors.field);
    println!("Number of test vectors: {}\n", vectors.vectors.len());
    
    let mut passed = 0;
    let mut failed = 0;
    
    for (i, vector) in vectors.vectors.iter().enumerate() {
        // For our simplified SHA256-based implementation, we can only test
        // if we're hashing the same way
        
        if vector.inputs.len() == 2 {
            let left = hex_to_hash256(&vector.inputs[0]);
            let right = hex_to_hash256(&vector.inputs[1]);
            let expected = hex_to_hash256(&vector.output);
            
            let computed = PoseidonHasher::hash_two(&left, &right);
            
            if computed == expected {
                passed += 1;
            } else {
                failed += 1;
                println!("MISMATCH at vector {}:", i);
                println!("  Inputs: {:?}", vector.inputs);
                println!("  Expected: {}", vector.output);
                println!("  Got:      0x{}", hex::encode(&computed));
                println!();
            }
        }
    }
    
    println!("Results: {} passed, {} failed out of {}", 
             passed, failed, vectors.vectors.len());
    
    if failed > 0 {
        println!("\n⚠ Note: Simplified SHA256 implementation may not match circuit Poseidon");
        println!("This is expected until proper Poseidon is implemented");
    }
}

